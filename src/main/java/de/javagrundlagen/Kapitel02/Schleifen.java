package de.javagrundlagen.Kapitel02;

public class Schleifen {

    public static void main(String[] args) {


        //WHILE SCHLEIFE
        int i = 1;
        while(i <= 5){
            System.out.println(i);
            i++;
        }

        //DO SCHLEIFE
        int j = 1;
        do {
            System.out.println(j);
            j++;
        }while (j<=5);

        //FOR SCHLEIFE
        for (int x = 1; x<=5; x++){
            System.out.println(x);
        }

        //FOR EACH SCHLEIFE

    }
}
